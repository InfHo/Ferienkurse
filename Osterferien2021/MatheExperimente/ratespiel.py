rechnung_2 = zahl_1 * zahl_2

rechnung_3 = zahl_1 - zahl_2

rechnung_4 = zahl_1 / zahl_2

print ("Addition:" , rechnung_1)

print ("Multiplikation:" ,rechnung_2)

print ("Subtraktion:", rechnung_3)

print ("Division:" ,rechnung_4)

